# Download data 
px_data <- 
  pxweb_get(url = "https://api.scb.se/OV0104/v1/doris/en/ssd/TK/TK1001/TK1001A/PersBilarDrivMedel",
            query = pxweb_query_list)

# Convert to data.frame 
px_data_frame <- as.data.frame(px_data, column.name.type = "text", variable.value.type = "text")

head(px_data_frame)
unique(px_data_frame$fuel)

filtered_data <- px_data_frame %>%
    filter(fuel %in% c("diesel", "electric hybrid", "electricity", "petrol", "plug-in hybrid"))

summary_data <- filtered_data %>%
  mutate(year = as.integer(substr(month, 1, 4))) %>% 
  group_by(year, fuel) %>%
  summarize(total_cars = sum(`New registered passenger cars`))


ggplot(summary_data, aes(x = year, y = total_cars, color = fuel, group = fuel)) +
  geom_point() + 
  geom_line() + 
  labs(title = "Nyregistrerade bilar baserade på drivmedel",
       x = "År",
       y = "Antal bilar") +
  scale_y_continuous(labels = function(x) format(x, scientific = FALSE)) + 
  theme_minimal()

